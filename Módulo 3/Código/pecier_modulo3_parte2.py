def analizar_circuito(voltaje, corriente):
    potencia = voltaje * corriente
    resistencia = voltaje / corriente if corriente else float('inf')
    estado = "OK" if potencia < 5000 else "SOBRECARGA"
    return potencia, resistencia, estado

