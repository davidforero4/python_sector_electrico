def diagnostico_motor(voltaje, corriente, rpm):
    potencia_entrada = voltaje * corriente
    potencia_mecanica = rpm * 0.1047 * corriente
    eficiencia = (potencia_mecanica / potencia_entrada) if potencia_entrada else 0
    estado = "NORMAL" if 0.7 <= eficiencia <= 0.9 else "REVISAR"

    return {
        "potencia_entrada": potencia_entrada,
        "potencia_mecanica": potencia_mecanica,
        "eficiencia (%)": round(eficiencia * 100, 2),
        "estado": estado
    }

reporte = diagnostico_motor(220, 8.5, 1750)
for clave, valor in reporte.items():
    print(f"{clave}: {valor}")

